					<p>1947 yılında başarılı bir bankacı olan Andy Dufrense aynı başarıyı evliliğinde gösterememektedir ve eşi tarafından başka bir erkekle aldatılmaktadır. Bunu kabullenemeyen Andy, eşinin aşığıyla buluştuğu bir gece alkolün de verdiği cesaretle silahına mermileri doldurur ve öfkeyle aracından iner. Eşi ve aşığının bulunduğu eve gidip onları belki de öldürecekken vazgeçer ve geriye döner. Fakat bu dönüş Andy'yi kurtaramaz. Aynı akşam eşi ve aşığı yataklarında mermi yağmuruna tutulmuşlardır. Olay yeri incelemelerinde Andy'nin ayak izlerine rastlayan polis, onu yargının eline teslim eder.
					</p>
					<p>Andy yargılanması sonucunda suçsuzluğunu ispatlayamaz ve iki kez ağırlaştırılmış müebbet hapse mahkûm edilir. Bir anda yaşamı altüst olur. Aldatılmıştır, eşi öldürülmüştür ve bu ölümde hiçbir suçu olmamasına rağmen artık hayatının geri kalanını hapiste geçirecektir. 1949 yılına gelindiğinde bir çatı yapım işi için arkadaşları ile seçilen ekipte yer alır. Burada çalışırken gardiyanların kaptanı Byron Hadley kendisine kalan bir mirastan arkadaşlarına bahseder ve devletin alacağı vergiden duyduğu rahatsızlıktan bahseder. Bu konuşmaya kulak misafiri olan Andy, Hadley'e doğru yönelerek onun bütün parasını kurtarabileceğini söyler ve nasıl kurtacağını anlatır. Bunun karşılığı olarak Andy arkadaşlarına bira alınmasını ister. Hadley bunu kabul eder ve çatıda çalışmakta olan bütün mahkûmlara bira ısmarlar. Mahkûmlar kısa bir süre de olsa Andy sayesinde kendilerini özgür birer insan gibi hissederler. Bu olaydan sonra Müdür Samuel Norton, Andy ile görüşür ve onu ihtiyar Brooks Halten'a yardım etmesi için cezaevinin kütüphanesine gönderir. Andy'nin yeni görevi kütüphanede çalışma bahanesi ile Norton'un para konusundaki işlerini halletmek olmuştur. Zaman geçtikçe Norton, Andy'e bütün işlerini emanet eder.
					</p>
					<p>Andy ise yeni geldiği kütüphaneyi yenilemek için her hafta devlete mektup yazarak kaynak ister. 1954'te Brooks şartlı tahliye edilir, ancak cezaevinde elli yıl geçirdiğinden dış dünyaya uyum sağlayamaz ve kendisini asarak intihar eder.

Andy'ye en sonunda kütüphane bağışı (kitaplar, dergiler, plak çalar, plaklar ve bir miktar para) gelir. Gelen plak çalar ile Figaro'nun Düğünü'nün bir kaydını bütün mahkûmlara dinletir. Ancak bunun bir cezası olur ve Andy tek kişilik hücreye gönderilir.
					</p>
					<p>1963 yılında Norton, kamusal işlerde hapishanede Andy'nin emeğini kullanmaya başlar, vasıflı iş gücünün maliyetini azaltarak rüşvet almaya başlar. Andy de Randall Stephens takma adını kullanarak bütün parayı kendine aktarmaktadır.
					</p>
					<p>
					1965'te Tommy Willams hırsızlık yaptığı için Shawshank hapishanesine düşer. Andy ve Red Tommy'e GED sınavını geçmesi için yardım eder. 1966 yılında Tommy, Andy'nin hikâyesini öğrenir ve başka bir hapishanede kalırken asıl suçu işleyen kişinin kendisine, Andy'nin karısını ve sevgilisini nasıl öldürdüğünü anlattığını Andy'ye anlatır. Andy suçsuz olduğunu müdür Norton'a anlatır ve Norton buna inanmak istemez ve Andy Norton'a hakaret eder. Norton da Andy'nin hücreye gönderilmesini emreder. Norton bir tuzak kurarak kaçıyormuş süsü verdirdiği Tommy'yi ortadan kaldırır. Norton hücresindeyken Andy'yi eski işlerine tekrar bakması şartı ile çıkaracağını söyler ve reddederse hem hücrede kalmakla hem de kütüphaneyi yakmakla tehdit eder. Bunun üzerine Andy Norton'un kirli işlerine devam etmek zorunda kalır. Eğer ki Andy hücreye girmemiş ve ceza almamış olsaydı 2 ay sonra şartlı tahliye edilebilirdi fakat şimdi cezasının tamamını çekmek zorunda kalmıştır.
					</p>
